GXSelectTile: ;; <14:TilePos>
		lda 	gxTilesOn 					; check tilemap in use
		beq 	_GXSFail

		.debug

		lda 	gxzTemp0 					; check X and Y in range
		cmp 	gxTileMapWidth
		bcs 	_GXSFail
		lda 	gxzTemp0+1
		cmp 	gxTileMapHeight
		bcs 	_GXSFail


		clc
_GXSFail:
		sec
		rts

		.send code

