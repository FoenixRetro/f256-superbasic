GXPlotPoint: ;; <40:Plot>
		jsr 	GXOpenBitmap 				; start drawing
		jsr 	GXPositionCalc 				; setup gxzScreen, gsOffset and the position.
		ldy 	gsOffset
		lda 	(gxzScreen),y 					; set pixel on the right
		.plotpixel
		sta 	(gxzScreen),y
		jsr 	GXCloseBitmap 				; stop drawing and exit
		clc
		rts

		.send code

