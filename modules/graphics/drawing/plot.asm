GXPlotPoint: ;; <40:Plot>
		jsr 	GXOpenBitmap 				; start drawing
		jsr 	gxPositionCalc 				; setup gxzScreen, gxOffset and the position.
		ldy 	gxOffset
		lda 	(gxzScreen),y 				; set pixel on the right
		.plotpixel
		sta 	(gxzScreen),y
		jsr 	GXCloseBitmap 				; stop drawing and exit
		clc
		rts

		.send code

