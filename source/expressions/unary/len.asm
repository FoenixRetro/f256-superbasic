LenUnary: ;; [len(]	
		plx 								; restore stack pos
		jsr 	EvaluateString 				; get a string
		phy
		ldy 	#0 							; find length
_LenFind:
		lda 	(zTemp0),y
		beq 	_LenExit
		iny
		bne 	_LenFind
		jmp 	RangeError 					; string > 255

_LenExit:
		tya		
		jsr 	NSMSetByte
		ply
		jsr 	CheckRightBracket
		rts

