Unary_Not: ;; [not(]
		plx
		jsr 	EvaluateInteger 			; get integer
		jsr 	CheckRightBracket
		jsr 	NSMIsZero 					; zero mantissa ?
		beq 	_NotZero
		jsr 	NSMSetZero
		rts

_NotZero: 									; return -1
		jmp 	ReturnTrue
_UNBad:
		jmp 	ArgumentError

		.send 	code
		
