SpcUnary: ;; [spc(]	
		plx 								; restore stack pos
		jsr 	Evaluate8BitInteger			; get value
		phy

		pha 								; save count
		jsr 	StringTempAllocate
		ply 								; to do count in Y
_SpcLoop:
		cpy 	#0
		beq 	_SpcExit		
		lda 	#32
		jsr 	StringTempWrite
		dey
		bra 	_SPCLoop
_SpcExit:
		ply
		jsr 	CheckRightBracket
		rts

