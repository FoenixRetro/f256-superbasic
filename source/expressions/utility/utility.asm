		.section code


; ************************************************************************************************
;
;							  Set mantissa to a 1 byte integer
;
; ************************************************************************************************

NSMMantissaZero:
		lda 	#0
NSMMantissaByte:
		sta 	NSMantissa0,x
		stz 	NSMantissa1,x
		stz 	NSMantissa2,x
		stz 	NSMantissa3,x
		stz 	NSExponent,x 				; zero exponent, as integer.
		stz 	NSStatus,x 					; status zero (integer)
		rts
				
; ************************************************************************************************
;
;									Shift the mantissa left
;
; ************************************************************************************************

NSMShiftLeft:		
		asl 	NSMantissa0,x
		rol		NSMantissa1,x
		rol		NSMantissa2,x
		rol		NSMantissa3,x
		rts

; ************************************************************************************************
;
;									Shift the mantissa right
;
; ************************************************************************************************

NSMShiftRight:		
		lsr 	NSMantissa3,x
		ror		NSMantissa2,x
		ror		NSMantissa1,x
		ror		NSMantissa0,x
		rts

		.send code

