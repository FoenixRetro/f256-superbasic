; ************************************************************************************************
;
;			Memory Peek: Read memory location at zTemp0/zTemp1 (4 byte address) -> A
;
; ************************************************************************************************

MemoryPeek:
		lda 	(zTemp0)
		rts

; ************************************************************************************************
;
;			Memory Poke : Write A to memory location at zTemp0/zTemp1 (4 byte address) 
;
; ************************************************************************************************

MemoryPoke:
		sta 	(zTemp0)
		rts

