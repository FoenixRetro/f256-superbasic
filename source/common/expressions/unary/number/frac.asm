; 								Integer part of number
;
; ************************************************************************************************

FracUnary: ;; [frac(]	
		plx 								; restore stack pos
		jsr 	EvaluateNumber 				; get a float or int
		jsr 	CheckRightBracket
		lda 	NSStatus,x
		and 	#NSTFloat 					; check it is a float
		beq 	_IUZero
		jsr 	FloatFractionalPart 		; if so, get the fractional part.
		rts
_IUZero:		
		jsr 	NSMSetZero
		rts

		.send code

