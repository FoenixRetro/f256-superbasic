		.section	code

; ************************************************************************************************
;
; 										Sign value (e.g. -1,0 or 1)
;
; ************************************************************************************************

SgnUnary: ;; [sgn(]	
		plx 								; restore stack pos
		jsr 	EvaluateNumber 				; get a float or int
		jsr 	CheckRightBracket
		jsr 	NSMIsZero 					; if zero
		beq 	_SGZero  					; return Int Zero
		;
		lda 	NSStatus,x 					; get status w/sign
		pha
		lda 	#1 							; set to 1
		jsr 	NSMSetByte
		pla
		and		#$80 						; copy the sign byte out
		sta 	NSStatus,x  				; so it will be -1 or 1
		rts

_SGZero:jsr 	NSMSetZero
		rts

		.send	code

