		.section code

AssertCommand: ;; [assert]
		ldx 	#0 							; bottom of stack
		jsr 	EvaluateInteger 			; the assert test
		jsr 	NSMIsZero 					; exit if result is non zero.
		bne 	_ACExit 
		.error_assert 						; otherwise assert an error.
_ACExit:
		rts		
		
		.send code

