		.section code

BootDOS:	;; [dos]
            lda     #dosName & $FF
            sta     kernel.args.buf+0
            lda     #dosName >> 8
            sta     kernel.args.buf+1
            jsr     kernel.RunNamed
            jmp 	WarmStart

dosName:	.text 	"DOS",0

		.send code

