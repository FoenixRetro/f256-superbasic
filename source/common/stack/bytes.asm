; ************************************************************************************************
;
;								Push A on the BASIC stack
;
; ************************************************************************************************

StackPushByte:
		pha 								; save byte
		lda 	BasicStack
		bne 	_SPBNoBorrow
		dec 	BasicStack+1
_SPBNoBorrow:
		dec 	BasicStack
		pla 								; get back and write
		sta 	(BasicStack)
		rts				

; ************************************************************************************************
;
;								Pop A off the BASIC stack
;
; ************************************************************************************************				

StackPopByte:
		lda 	(BasicStack)
		inc 	BasicStack
		bne 	_SPBNoCarry
		inc 	BasicStack+1
_SPBNoCarry:
		rts
					
