UnaryPlaying: ;; [playing(]
		plx
		;
		jsr 	Evaluate8BitInteger 		; get channel #
		pha
		jsr		CheckRightBracket
		pla
		cmp 	#4
		bcs 	_UPNotPlaying
		ora 	#$20 						; query playing ?
		jsr 	SNDCommand
		cmp 	#0
		beq 	_UPNotPlaying
		jmp 	ReturnTrue
_UPNotPlaying:
		jmp 	ReturnFalse		

		.send code

