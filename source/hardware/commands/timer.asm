UnaryTimer: ;; [timer(]
		plx
		jsr 	CheckRightBracket
TimerToStackX:		
		jsr 	NSMSetZero 					; zero result
		stz 	1 							; access I/O
		lda 	$D659 						; copy timer in
		sta 	NSMantissa0,x
		lda 	$D65A
		sta 	NSMantissa1,x
		lda 	$D65B
		sta 	NSMantissa2,x
		rts

		.send code

